
#include <iostream>
using namespace std;

int main() {
    int age = 25; // Declare an integer variable named 'age' and initialize it to 25
    double height = 5.9; // Declare a double variable named 'height' and initialize it to 5.9
    char initial = 'A'; // Declare a character variable named 'initial' and initialize it to 'A'
    bool isStudent = true; // Declare a boolean variable named 'isStudent' and initialize it to true
    float gpa = 3.75; // Declare a float variable named 'gpa' and initialize it to 3.75
    double pi = 3.14159; // Declare a double variable named 'pi' and initialize it to 3.14159


    // Output the values of the variables
    cout << "Age: " << age << endl;
    cout << "Height: " << height << endl;
    cout << "Initial: " << initial << endl;
    cout << "Is Student: " << (isStudent ? "Yes" : "No") << endl;
    cout << "GPA: " << gpa << endl;
    cout << "Value of Pi: " << pi << endl;

    return 0;
    
}